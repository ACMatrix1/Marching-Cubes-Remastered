    //Converts Values into the mesh Combination
    public void VertexProcessing(Vector3 Point)
    {
        int cubeIndex = 0;

        for (int t = 0; t < vertexVals.Length; t++)
        {
            if (vertexVals[t] > Threshold)
            {
                cubeIndex |= 1 << t;
            }
        }

        print(cubeIndex);

        int edgeindex = 0;

        //The length = 5 Because in any one combination there is no more than 5 Triangles (T stands for triangles)
        for (int T = 0; T < 5; T++)
        {
            //The Length = 3 because there are only ever 3 verticies on a triangle
            for (int V = 0; V < 3; V++)
            {
                int CombinationIndex = TriangleTable[cubeIndex, edgeindex];

                if (CombinationIndex != -1)
                {
                    Vector3 V1 = Point + VertTable[CombinationIndex, 0];
                    Vector3 V2 = Point + VertTable[CombinationIndex, 1];

                    Vector3 VertexPosition = (V1+V2)/2;

                    verticies.Add(VertexPosition);
                    Triangles.Add(verticies.Count - 1);

                    edgeindex++;
                }
            }
        }
    }

    public void ConstructMesh()
    {

        GeneratedMesh = new Mesh();
