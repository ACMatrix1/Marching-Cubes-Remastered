    public static float Perlin3D(float x, float y, float z, float density, float scale)
    {
        float XY = Mathf.PerlinNoise(x * density, y * density);
        float YZ = Mathf.PerlinNoise(y * density, z * density);
        float ZX = Mathf.PerlinNoise(z * density, x * density);

        float YX = Mathf.PerlinNoise(y * density, z * density);
        float ZY = Mathf.PerlinNoise(z * density, y * density);
        float XZ = Mathf.PerlinNoise(x * density, z * density);

        float val = (XY + YZ + ZX + YX + ZY + XZ) / 6f;
        return val * scale;
    }

